class Type_casting_operator 
{
	public static void main(String[] args) 
	{
		int a = 97;
		char ch = (char)a;
		//variable
		System.out.println(ch);
		char ch1=(char)122;
		//value
		System.out.println(ch1);
		int c =97;
		int d =20;
		char ch2 = (char)(c+d);
		//expression
		System.out.println(ch2);


	}
}
